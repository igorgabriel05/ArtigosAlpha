    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        try {
        String produto = txtProduto.getText();
        String tipo = txtTipo.getText();
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        double preco = Double.parseDouble(txtPreco.getText());
        boolean valorInvalido = false;

        if (quantidade < 0 || preco < 0.0) {
            valorInvalido = true;
            JOptionPane.showMessageDialog(rootPane, "Quantidade ou Valor invalido!!\nNão pode ser numero negativo.");
        }
        if (valorInvalido) {
            return;
        }
        Object[] novoProduto = new Object[]{
            produto, tipo, quantidade, preco
        };
        Produtos produtos = new Produtos();
        produtos.setProduto(produto);
        produtos.setTipo(tipo);
        produtos.setQuantidade(quantidade);
        produtos.setPreco(preco);

        ProdutosDAO produtosDAO = new ProdutosDAO();
        produtosDAO.adicionar(produtos);
        txtProduto.setText("");
        txtTipo.setText("");
        txtQuantidade.setText("");
        txtPreco.setText("");
        JOptionPane.showMessageDialog(rootPane, "Produto adicionado com sucesso.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao adicionar produto!!");
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

