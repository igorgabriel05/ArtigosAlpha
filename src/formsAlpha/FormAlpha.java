    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
        int id = Integer.parseInt(txtBuscarProd.getText());
        ProdutosDAO produtosDAO = new ProdutosDAO();
        Produtos produtos = produtosDAO.getProdutos(id);
        
            
        if(produtos == null) {
            JOptionPane.showMessageDialog(rootPane, "ID não encontrado.");
            txtCodigo.setText("");
            txtProduto.setText("");
            txtTipo.setText("");
            txtQuantidade.setText("");
            txtPreco.setText("");
        } else {
            txtCodigo.setText(String.valueOf(produtos.getId()));
            txtProduto.setText(produtos.getProduto());
            txtTipo.setText(produtos.getTipo());
            txtQuantidade.setText(String.valueOf(produtos.getQuantidade()));
            txtPreco.setText(String.valueOf(produtos.getPreco()));
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Insira somente numeros inteiros!!");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

